package ru.hemulen.docsigner.model;

public class Resp {
    String ossSimConfirm;
    String ossNam;
    String inn;
    String phoneNumber10;
    String PhoneNumber14;
    String uki;

    public Resp() {
    }

    public String getOssSimConfirm() {
        return ossSimConfirm;
    }

    public void setOssSimConfirm(String ossSimConfirm) {
        this.ossSimConfirm = ossSimConfirm;
    }

    public String getOssNam() {
        return ossNam;
    }

    public void setOssNam(String ossNam) {
        this.ossNam = ossNam;
    }

    public String getInn() {
        return inn;
    }

    public void setInn(String inn) {
        this.inn = inn;
    }

    public String getPhoneNumber10() {
        return phoneNumber10;
    }

    public void setPhoneNumber10(String phoneNumber10) {
        this.phoneNumber10 = phoneNumber10;
    }

    public String getPhoneNumber14() {
        return PhoneNumber14;
    }

    public void setPhoneNumber14(String phoneNumber14) {
        PhoneNumber14 = phoneNumber14;
    }

    public String getUki() {
        return uki;
    }

    public void setUki(String uki) {
        this.uki = uki;
    }
}
