package ru.hemulen.docsigner.model;

public class Dul {
    private String docIssOn;
    private String docNum;
    private String brd;
    private String docType;
    private String docSer;

    public String getDocIssOn() {
        return docIssOn;
    }

    public void setDocIssOn(String docIssOn) {
        this.docIssOn = docIssOn;
    }

    public String getDocNum() {
        return docNum;
    }

    public void setDocNum(String docNum) {
        this.docNum = docNum;
    }

    public String getBrd() {
        return brd;
    }

    public void setBrd(String brd) {
        this.brd = brd;
    }

    public String getDocType() {
        return docType;
    }

    public void setDocType(String docType) {
        this.docType = docType;
    }

    public String getDocSer() {
        return docSer;
    }

    public void setDocSer(String docSer) {
        this.docSer = docSer;
    }
}
